const oklchValuePattern =
  /^oklch\(\s*(-?\d*\.?\d+%?)\s+(-?\d*\.?\d+)\s+(-?\d*\.?\d+)(?:deg)?(?:\s*\/\s*[^)]+)?\s*\)$/

